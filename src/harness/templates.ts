export function projectAgentsMd(profile: DomainProfile): string {
  const stack = [
    profile.stack?.backend && `Backend: ${profile.stack.backend}`,
    profile.stack?.frontend && `Frontend: ${profile.stack.frontend}`,
    profile.stack?.infra && `Infra: ${profile.stack.infra}`,
  ]
    .filter(Boolean)
    .join(" · ");

  return `# Project Agent Harness (aio-mcp)

> Auto-generated by \`bootstrap_harness\`. Domain: **${profile.domain}** — ${profile.description}

## Before any implementation

1. Call MCP **\`bootstrap_domain\`** with the user's task (or **\`run_domain_loop\`** for full loop brief).
2. Read returned wiki pages and **cite** \`[[page-title]]\` in your plan.
3. Call **\`plan_task\`** for multi-step work; **\`execute_dag\`** when tasks are independent.
4. After durable decisions, **\`file_back\`** into the wiki.
5. Run **\`lint_wiki\`** when wiki structure may have changed.

## Hard rules

- Do **not** invent domain facts missing from wiki/raw.
- Respect **bounded contexts** from wiki (do not merge unrelated domains in one module).
- ${profile.loop?.require_citations !== false ? "Answers must include wiki citations." : "Prefer wiki citations."}
${stack ? `\n## Stack\n\n${stack}\n` : ""}
## MCP tools (priority)

${(profile.harness?.suggested_mcp_tools || [])
  .map((t) => `- \`${t}\``)
  .join("\n")}

## Loop

${(profile.loop?.steps || [])
  .map((s, i) => `${i + 1}. ${s}`)
  .join("\n")}

Re-run \`bootstrap_harness\` after changing \`.aio/domain-profile.yaml\`.
`;
}

