export function cursorSessionStartHook(): string {
  return `#!/usr/bin/env node
/**
 * aio-mcp sessionStart — inject cached domain context from .aio/harness-context.json
 */
import fs from "fs";
import path from "path";

const root = process.cwd();
const cache = path.join(root, ".aio", "harness-context.json");
const profilePath = path.join(root, ".aio", "domain-profile.yaml");

let additional = "## aio-mcp harness\\n\\n";
additional += "Before coding, call MCP bootstrap_domain or run_domain_loop for the user task.\\n";
additional += "Follow AGENTS.md and vault wiki bounded contexts.\\n";

try {
  if (fs.existsSync(cache)) {
    const pack = JSON.parse(fs.readFileSync(cache, "utf8"));
    additional += "\\n### Cached domain context\\n";
    additional += "Task: " + (pack.task || "(none)") + "\\n";
    if (pack.pages?.length) {
      additional += "Wiki pages: " + pack.pages.map((p) => p.title).join(", ") + "\\n";
    }
    if (pack.harness_prompt) {
      additional += "\\n" + pack.harness_prompt.slice(0, 2000);
    }
  }
} catch {
  /* ignore */
}

try {
  if (fs.existsSync(profilePath)) {
    additional += "\\n(domain-profile.yaml present at .aio/)";
  }
} catch {
  /* ignore */
}

console.log(JSON.stringify({ additional_context: additional }));
`;
}

export function cursorBeforePromptHook(): string {
  return `#!/usr/bin/env node
/**
 * aio-mcp beforeSubmitPrompt — remind agent to bootstrap wiki for implementation-like prompts
 */
import fs from "fs";

let input = "";
process.stdin.setEncoding("utf8");
for await (const chunk of process.stdin) input += chunk;

let prompt = "";
try {
  const data = JSON.parse(input || "{}");
  prompt = data.prompt || data.text || "";
} catch {
  prompt = input;
}

const impl = /\\b(구현|만들|작성|추가|리팩|refactor|implement|create|build|fix|api|spring|react)\\b/i;
const cacheExists = fs.existsSync(".aio/harness-context.json");

if (impl.test(prompt) && !cacheExists) {
  console.log(
    JSON.stringify({
      additional_context:
        "aio-mcp: Call bootstrap_domain (or run_domain_loop) BEFORE writing code. Load wiki context for this task.",
    })
  );
} else {
  console.log("{}");
}
`;
}

